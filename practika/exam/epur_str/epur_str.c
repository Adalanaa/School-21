#include <unistd.h>

int main(int ac, char **av)
{
	int 	i;
	char 	c;

	i = 0;
	c = '\n';
	if (ac == 2)
	{
		while (av[1][i] == ' ' || av[1][i] == '\0' || av[1][i] == '\n' || av[1][i] == '\t')
			i++;
		while (av[1][i])
		{
			if (av[1][i] != ' ' && av[1][i] != '\0' && av[1][i] != '\n' && av[1][i] != '\t')
				write(1, &(av[1][i]), 1);
			if ((av[1][i] == ' ' || av[1][i] == '\0' || av[1][i] == '\n' || av[1][i] == '\t')\
			&& av[1][i - 1] != ' ' && av[1][i] != '\0' && av[1][i] != '\n' && av[1][i] != '\t' && av[1][i + 1] != '\0')
			{
				av[1][i] = ' ';
				write(1, &(av[1][i]), 1);
			}
			i++;
		}
	}
	write(1, &c, 1);
	return 0;
}