int	ft_sqrt(int nb)
{
	int sq;

	sq = 0;
	if (nb > 0 && nb < 2147395601)
	{
		while (sq <= nb)
		{
			if (sq * sq == nb)
				return (sq);
			else if (sq * sq > nb)
				return (0);
			else
				sq++;
		}
		sq = 0;
	}
	return (sq);
}
